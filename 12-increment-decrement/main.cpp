#include <iostream>

using namespace std;

int main()
{
    int a = 5;

    // ada 2 tipe, pre & post, pre itu sebelum dan post itu sesudah

    cout << a << endl;
    cout << ++a << endl;
    cout << a++ << endl;
    cout << a << endl;

    cout << a << endl;
    cout << --a << endl;
    cout << a-- << endl;
    cout << a << endl;

    return 0;
}
